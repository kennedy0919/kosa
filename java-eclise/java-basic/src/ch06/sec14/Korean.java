package ch06.sec14;

public class Korean {
    private String name;
    private String ssn;
    private int age;
    private boolean male;

    public Korean(String name, String ssn, int age, boolean male) {
        this.name = name;
        this.ssn = ssn;
        this.age = age;
        this.male = male;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSsn() {
        return ssn;
    }

    public void setSsn(String ssn) {
        this.ssn = ssn;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public boolean isMale() {
        return male;
    }

    public void setMale(boolean male) {
        this.male = male;
    }

    
}
